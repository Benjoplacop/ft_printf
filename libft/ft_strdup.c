#include "libft.h"

char	*ft_strdup(const char *src)
{
	char	*dest;
	size_t	len;

	len = ft_strlen(src) + 1;
	dest = malloc(sizeof(*dest) * len);
	if (dest == 0)
		return (NULL);
	return ((char *)ft_memcpy(dest, src, len));
}
